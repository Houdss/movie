const languageSelect = document.getElementById('language-select').querySelector('select');
const titleElement = document.querySelector('.title');
const descriptionElement = document.querySelector('.description');
const loginButton = document.getElementById('login-button').querySelector('a');
const collapsibleHeaders = document.querySelectorAll('.profile-container h4');
const collapsibleContents = document.querySelectorAll('.collapsible-content');
const footerLinks = document.querySelectorAll('.footer-links a');

// Function to update content
function updateContent(language) {
    const texts = translations[language];
    
    // Update title and description
    titleElement.textContent = texts.title;
    descriptionElement.textContent = texts.description;

    // Update login button text
    loginButton.textContent = texts.loginButton;

    // Update FAQ questions and answers
    collapsibleHeaders.forEach(header => {
        const id = header.getAttribute('id');
        if (texts.questions[id]) {
            header.innerHTML = `${texts.questions[id]} <span>+</span>`;
        }
        const content = header.nextElementSibling;
        if (content) {
            content.querySelector('p').textContent = texts.answers[id];
        }
    });

    // Update footer links
    footerLinks.forEach(link => {
        const footerId = link.getAttribute('data-footer-id');
        if (texts.footer[footerId]) {
            link.textContent = texts.footer[footerId];
        }
    });

    // Hide all collapsible contents initially
    collapsibleContents.forEach(content => {
        content.style.display = 'none';
    });
}

// Function to toggle collapsible content
function toggleCollapsible(event) {
    const header = event.target.closest('h4');
    if (!header) return;

    const content = header.nextElementSibling;
    if (content) {
        const isVisible = content.style.display === 'block';
        content.style.display = isVisible ? 'none' : 'block';
        header.querySelector('span').textContent = isVisible ? '+' : '−';
    }
}

// Event listener for language change
languageSelect.addEventListener('change', function() {
    updateContent(this.value);
});

// Event listener for collapsible headers
collapsibleHeaders.forEach(header => {
    header.addEventListener('click', toggleCollapsible);
});

// Initialize content
updateContent(languageSelect.value || 'en');